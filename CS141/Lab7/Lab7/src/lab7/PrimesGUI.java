    /**
     * ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
     * <pre>
     * Method       isPrime
     * Description  Method to determine is given parameter is a prime number.
     * Date         5/18/2021
     * @return      true/false boolean
     * @author      <i>Robert Tang</i>
     * </pre>
     * ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
     */
    private boolean isPrime(long number) {
        if (number == 2) {
            return true;
        } else if (number % 2 == 0) {
            return false;
        } else {
            // check for odd factors only up to square root of the number
            double root = Math.sqrt(number);

            for (int i = 3; i <= root; i += 2) {
                if (number % i == 0) {
                    return false;
                }
            }

            return true;
        }

    }	// end isPrime

    /**
     * ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
     * <pre>
     * Method       isPalindrome
     * Description  Method to determine is given number is a palindrome.
     * Date         5/18/2021
     * @param number-int
     * @return true/false boolean
     * @author      <i>Robert Tang</i>
     * </pre>
     * ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
     */
    public boolean isPalindrome(int number) {

        return number == this.reverse(number);
    }

    /**
     * ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
     * <pre>
     * Method       reverse
     * Description  Method to return the reverse of a number.
     * Date         5/18/2021
     * @return      reverse int
     * @author      <i>Robert Tang</i>
     * </pre>
     * ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
     */
    private int reverse(int number) {
        int result = 0;

        while (number != 0) {
            int lastDigit = number % 10;
            result = result * 10 + lastDigit;
            number = number / 10;
        }

        return result;
    }

